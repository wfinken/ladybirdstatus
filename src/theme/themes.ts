export function normalizeSelection(input: Partial<ThemeSelection> | null | undefined): ThemeSelection {
  const style = isVisualStyle(input?.style) ? input.style : DEFAULT_THEME_SELECTION.style;
  const mode = isColorMode(input?.mode) ? input.mode : DEFAULT_THEME_SELECTION.mode;
  const rawPalette = isPaletteId(input?.palette) ? input.palette : DEFAULT_THEME_SELECTION.palette;
  const palette = resolveCompatiblePalette(style, rawPalette);

  return { style, palette, mode };
}
