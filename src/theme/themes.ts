const styleById: Record<VisualStyle, ThemeStyleDefinition> = STYLE_DEFINITIONS.reduce(
  (acc, style) => {
    acc[style.id] = style;
    return acc;
  },
  {} as Record<VisualStyle, ThemeStyleDefinition>,
);

const paletteById: Record<PaletteId, ThemePaletteDefinition> = PALETTE_DEFINITIONS.reduce(
  (acc, palette) => {
    acc[palette.id] = palette;
    return acc;
  },
  {} as Record<PaletteId, ThemePaletteDefinition>,
);

export function isVisualStyle(value: unknown): value is VisualStyle {
  return typeof value === 'string' && value in styleById;
}

export function isPaletteId(value: unknown): value is PaletteId {
  return typeof value === 'string' && value in paletteById;
}

export function isColorMode(value: unknown): value is ColorMode {
  return value === 'light' || value === 'dark' || value === 'system';
}

export function getStyleDefinition(style: VisualStyle): ThemeStyleDefinition {
  return styleById[style];
}

export function getAllPalettes(): ThemePaletteDefinition[] {
  return PALETTE_DEFINITIONS;
}

export function getPalettesForStyle(style: VisualStyle): ThemePaletteDefinition[] {
  return STYLE_PALETTE_COMPAT[style].map((id) => paletteById[id]);
}

export function resolveCompatiblePalette(style: VisualStyle, palette: PaletteId): PaletteId {
  const allowed = STYLE_PALETTE_COMPAT[style];
  if (allowed.includes(palette)) {
    return palette;
  }
  return STYLE_DEFAULT_PALETTE[style];
}

export function resolveMode(mode: ColorMode, systemPrefersDark: boolean): ResolvedMode {
  if (mode === 'system') {
    return systemPrefersDark ? 'dark' : 'light';
  }
  return mode;
}

